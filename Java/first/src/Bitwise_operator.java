public class Bitwise_operator {
    public static void main(String[] args)
    {
        int a=32;
        int b=12;
        int c=a&b;
        System.out.println(c);
        c=a|b;
        System.out.println(c);
        c=a^b;
        System.out.println(c);
        c=a>>2;
        System.out.println(c);

    }
}
